public class ВыводДанных {
    public static void main(String[] args) {
        // Выводим строку
        System.out.println("Привет, мир!");

        // Выводим переменные
        String имя = "John";
        int возраст = 25;
        System.out.println("Имя: " + имя + ", Возраст: " + возраст);

        // Форматированный вывод
        double число = 3.14159;
        System.out.printf("Значение числа Pi: %.2f", число);
    }
}

